// Helper function to calculate the square of a number
function calculateSquare(number) {
    return number * number;
  }
  
  // Main function to demonstrate the helper function
  function main() {
    // Sample number
    let inputNumber = 5;
  
    // Calculate the square using the helper function
    let result = calculateSquare(inputNumber);
  
    // Display the result
    console.log(`The square of ${inputNumber} is: ${result}`);
  }
  
  // Call the main function to run the code
  main();
  